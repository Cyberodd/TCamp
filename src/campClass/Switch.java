package campClass;

public class Switch {
	public static void main(String[] args) {
		int y= 70;
		
		switch(y) {
		case 70:
			System.out.println("Number is high");
			break;
			
		case 50:
			System.out.println("Number is average");
			break;
			
		case 30:
			System.out.println("Number is low");
			break;
		
		default:
			break;
		}
	}
	
}
